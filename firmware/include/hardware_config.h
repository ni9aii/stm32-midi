#ifndef HARDWARE_CONFIG_H
#define HARDWARE_CONFIG_H

#include <libopencm3/stm32/gpio.h>

#define STM32_MIDI_SYSTEM_CORE_CLOCK_HZ 72000000u

/* USB D- = PA11, USB D+ = PA12 (used with GPIOA in gpio_setup) */
#define STM32_MIDI_USB_D_MINUS_PIN GPIO11
#define STM32_MIDI_USB_D_PLUS_PIN GPIO12

/* Row demux (74HC238): PB0=A0, PB1=A1, PB11=A2 — used with GPIOB */
#define STM32_MIDI_ROW_SELECT_PIN_MASK (GPIO0 | GPIO1 | GPIO11)
#define STM32_MIDI_ROW_SELECT_COUNT 3u

/* Column mux (74HC151): PA1=S0, PA2=S1, PB12=S2; output read from PB13 */
#define STM32_MIDI_MUX_SELECT_COUNT 3u
#define STM32_MIDI_MUX_SELECT_PA_PIN_MASK (GPIO1 | GPIO2)
#define STM32_MIDI_MUX_SELECT_PB_PIN GPIO12
#define STM32_MIDI_MUX_OUTPUT_PIN GPIO13

#define STM32_MIDI_USB_MIDI_EP_IN 0x81u
#define STM32_MIDI_USB_MIDI_EP_SIZE 64u

#endif
